				<h1>Bienvenidos</h1>
				
			</section>

			<section class="pageSection" id="historia" style="display:none;">
			<span class="historia">En  el mes de diciembre de 2000,  productores y conductores del programa  “Acoso radial”, nos desvinculábamos de la emisora F.M. Láser,  propiedad de la empresa de la familia Segovia, que también irradia  la señal de L.U. 12, AM 680, Radio Río Gallegos y editan del diario  La Opinión Austral.
			<br><br>
			En  dicha empresa pusimos al aire el programa de referencia, sin  interrupciones por más de tres años, junto con el programa “El  71”, que se emitía de lunes a viernes de 21:30 a 23:00 horas,.por  LU 12 Radio Río Gallegos
			<br><br>
			A  esta altura “acoso radial” tenía mas de 8 años de permanencia,  habíamos recorrido tres emisoras y ganado un espacio de 9 a 13 horas  que nos ponía entre los programas mas escuchados de la mañana  riogalleguense.
			<br><br>
			<span style="text-align:right;padding-left:10px;margin:10px;padding-top:15px;"><img src="http://www.fmabril.net/contenido/imagenes/historia1.jpg" style="text-align:right;padding-left:10px;margin:10px;padding-top:15px;" align="right" border="0" alt="FM Abril"></span>
			La  desvinculación se presento de manera natural, y mas allá de la  resignación económica que implicaba no renovar el contrato, pesaba  sobre ambas partes la interrupción de una diaria relación personal,  que generó una excelente amistad hasta el día de hoy
			<br><br>
			Pero  todo estaba impregnado por una sensación, una necesidad de dar un  paso importante en un contexto nuevo, solo conocido en parte, pero  una parte fundamental, un conjunto de “contenidos” que súper  poblaban nuestra mesa de trabajo y que se fueron gestando  gradualmente dentro del programa a partir de los elementos  siguientes:
			<br><br>
			Proponíamos,  como lo es actualmente, un ámbito donde se expresaran todas las  voces posibles. La mayoría del contenido era generado por nosotros,  a través de entrevistas, reportajes, producciones conceptuales,  radioteatros, personajes, murgas, e incluso la pauta comercial -que  estaba armada en un estudio creado a esos fines- respondía a la  estética general del programa, de manera que este concepto,  escasamente utilizado hasta el momento, generó una respuesta  extraordinaria en el aspecto publicitario que transformamos  inmediatamente en un mejoramiento de la tecnología disponible para  la producción.
			<br><br>
			Asimismo,  desfilaron por el programa importantes referentes de la cultura, la  política, del arte, del deporte, y en mérito a una aceptable  comprensión de los requerimientos de la audiencia, instalamos  bloques específicos para cada género que enriquecieron nuestro  producto y llevaban a “acoso radial” a una dimensión que  tropezaba invariablemente con el escaso tiempo disponible para el  conveniente tratamiento del material.
			<br><br>
			Trabajamos  entonces en un ordenamiento de los contenidos, tomamos contacto con  periodistas, locutores y productores de otros programas, hablamos del  plan, las misiones y objetivos, y solicitamos su ayuda para rearmar  cada segmento del programa principal en otros más específicos.
			Todo  iba tomando forma de radio.
			<br><br>
			<span style="text-align:left;padding-left:10px;margin:10px;padding-top:15px;"><img src="http://www.fmabril.net/contenido/imagenes/historia3.jpg" align="left" border="0" alt="FM Abril" style="text-align:right;padding-left:10px;margin:10px;padding-top:15px;"></span>
			Ya  en los albores del 2001, habíamos logrado diseñar media docena de  programas con identidad propia, con contenido de verdadero interés  para un público determinado y con una idea principal, sostenida por  consenso, que derramaba sobre cada propuesta un sutil toque de ironía  en un interminable mar de libre expresión, tal vez el origen de lo  que derivó luego en el inconfundible estilo que hoy define a FM  Abril.
			<br><br>
			Provistos  de contenido, habilitado un estudio de grabación y con la necesidad  imperiosa de trabajar, decidimos finalmente encarar la inversión  necesaria para adquirir el equipamiento y realizar la obra civil que  permitiera poner al aire una señal de frecuencia modulada, y ofrecer  a consideración del público los distintos programas, y la respuesta  fue la que esperábamos,  no obstante, conmovedora.
			<br><br>
			“<em>Un  conjunto de hombres y mujeres vinculados con la tarea periodística,  con el arte, con la tecnología, que a partir de su experiencia y sus  valores, toman contacto a través de la radio con la comunidad para  informarla, entretenerla, acompañarla y propender a su mejoramiento,  con la convicción de que es necesario insistir y en el mejor de los  casos educar, sobre el valor de la <strong>libre</strong></em> <em><strong>expresión</strong>,  dejar claro qué sucede cuando este <strong>derecho</strong> no se tiene, y a través del ejemplo, sobre como <strong>ejercerla</strong> y como <strong>cuidarla</strong></em>
			<br><br>
			</span>
